
public class Swithcase {

	public static void main(String[] args)
	{
		int a=30;
		switch(a)
		{
		case 45: System.out.println("a is equal to 45");
		case 30: System.out.println("a is equal to 30");
		case 75: System.out.println("a is equal to 75");
		break;
		case 93: System.out.println("a is equal to 93");
		case 12: System.out.println("a is equal to 12");
		default: System.out.println("a is equal to default");
		}
	System.out.println("after switch case");

	}

}
